import CoreGraphics

/// Default layout metric constants
public enum LayoutGrid {

    /// Base layout module
    public static let module: CGFloat = 8.0

    /// Half layout module
    public static let halfModule: CGFloat = module / 2.0

    /// Double layout module
    public static let doubleModule: CGFloat = module * 2.0

    /// Triple layout module
    public static let tripleModule: CGFloat = module * 3.0

    /// Quadruple layout module
    public static let quadrupleModule: CGFloat = module * 4.0

}
